import 'package:flutter/material.dart';

/// The [DaySeparatorStyle] class is used by the default [DaySeparator] widget.
class DaySeparatorStyle {
  const DaySeparatorStyle({
    this.thickness,
    this.color,
  });

  /// The thickness of the day separator.
  final double? thickness;

  /// The day separator color.
  final Color? color;
}
